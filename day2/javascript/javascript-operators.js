// simple assignment operators
let x = 10;
console.log(x);

// addition assignment operators
x += 5;
console.log(x);

let text = "hello";
text += "world";
console.log(text);

// subtract assignment operators
x -= 5;
console.log(x);

// multiplication assignment operators
x *= 5;
console.log(x);

// exponential assignment operators
x **= 5;
console.log(x);

// division assignment operators
x /= 5;
console.log(x);

// remainder assignment operators
x %= 5;
console.log(x);

// left shift assignment operators
let y = -10;
y <<= 2;
console.log(y);

// right shift assignment operators
y >>= 4;
console.log(y);

// unsigned right shift assignment operators
let z = -2;
z >>>= 3;
console.log(z);

// bitwise AND assignment operators
console.log("bitwise");
let a = 10;
a &= 2;
console.log(a);

// bitwise OR assignment operators
let a1 = 2;
a1 |= 10;
console.log(a1);

// bitwise XOR assignment operators
let bit = 10;
bit ^= 5;
console.log(bit);

// logical AND assignment operators
let g = 10;
g &&= 5;
console.log(g);

// logical OR assignment
let s = 10;
s ||= 5;
console.log(s);

// Nullish coalescing assignment operators
let nu;
nu ??= 10;
console.log(nu);
